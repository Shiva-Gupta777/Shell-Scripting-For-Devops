#!/bin/bash

# =========================================
# 1. Simple Function
# =========================================
say_hello() {
  echo "Hello Shiva"
}

say_hello

echo "----------------------------------"

# =========================================
# 2. Function with Argument
# =========================================
greet() {
  echo "Hello $1"
}

greet Shiva
greet Rahul

echo "----------------------------------"

# =========================================
# 3. Multiple Arguments
# =========================================
info() {
  echo "Name: $1"
  echo "Age: $2"
}

info Shiva 21

echo "----------------------------------"

# =========================================
# 4. Return Value using echo
# =========================================
sum() {
  result=$(($1 + $2))
  echo $result
}

ans=$(sum 10 5)
echo "Sum is $ans"

echo "----------------------------------"

# =========================================
# 5. Local Variable
# =========================================
test_func() {
  local name="Local Shiva"
  echo $name
}

test_func

echo "----------------------------------"

# =========================================
# 6. DevOps Example
# =========================================
install() {
  echo "Installing $1..."
}

install nginx
install git

echo "----------------------------------"

